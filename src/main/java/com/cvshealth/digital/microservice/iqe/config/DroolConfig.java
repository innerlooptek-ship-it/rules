package com.cvshealth.digital.microservice.iqe.config;

import org.kie.api.KieServices;
import org.kie.api.builder.KieBuilder;
import org.kie.api.builder.KieFileSystem;
import org.kie.api.builder.KieModule;
import org.kie.api.runtime.KieContainer;
import org.kie.internal.io.ResourceFactory;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
public class DroolConfig {
    public static final String QUESTIONNAIRE_RULES_DRL = "rules/questionnaire-condition.drl";
    public static final String QUESTIONNAIRE_TEMPLATE_FILE = "rules/questionnaire-output.drl";
    public static final String QUESTIONNAIRE_TEMPLATE_FILE_IQE = "rules/getQuestionnaire-output.drl";
    private static final KieServices kieServices = KieServices.Factory.get();

    @Bean
    public KieContainer kieContainer() {
        KieFileSystem kieFileSystem = kieServices.newKieFileSystem();
        kieFileSystem.write(ResourceFactory.newClassPathResource(QUESTIONNAIRE_RULES_DRL));
        KieBuilder kb = kieServices.newKieBuilder(kieFileSystem);
        kb.buildAll();
        KieModule kieModule = kb.getKieModule();
        return kieServices.newKieContainer(kieModule.getReleaseId());
    }


}